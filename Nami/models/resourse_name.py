class objeto_usuario:
 def __init__(self):
     self.id = None
     self.nombre = None
     self.correo = None
     
 
 def contructor_usuario(self, id, nombre, correo): 
     self.id = id 
     self.nombre = nombre
     self.correo = correo
     
 def set_id_usuario(self , id):
     
     self.id = id
     
 def get_id_usuario(self) :
     
     return self.id
     
 def set_nombre(self , nombre ): 
     
     self.nombre = nombre
     
 def get_nombre(self):
     
     return self.nombre  
     
 def set_correo(self, correo):
     
     self.correo = correo
     
 def get_correo(self):
     
     return self.correo
     
     
     
     
    